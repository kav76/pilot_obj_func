// Get pilot name function
function get_pilot_name (pilot) {
    return pilot.name;
}

// Get pilot age function
function get_pilot_age (pilot) {
    return pilot.age;
}

// Set pilot name function
function set_pilot_name (pilot, name) {
    pilot.name = name;
}

// Set pilot age function
function set_pilot_age (pilot, age) {
    pilot.age = age;
}